export default (key: string | undefined, translation_type: string, default_value: string = ""): string => {
    let translation_data: TranslationMap;

    if (!key) {
        return "";
    }

    switch (translation_type) {
        case "Persona":
            translation_data = personaTranslationData;
            break;
        case "PersonaImg":
            translation_data = personaImgData;
            break;
        case "PersonaNickName":
            translation_data = personaNickNameData;
            break;
        case "PersonaPersonality":
            translation_data = personaPersonalityData;
            break;
        case "Skill":
            translation_data = skillTranslationData;
            break;
        case "Arcana":
            translation_data = arcanaTranslationData;
            break;
        case "SkillEfc":
            translation_data = skillEfcTranslationData;
            break;
        case "Elem":
            translation_data = elemTranslationData;
            break;
        case "ElemEfc":
            translation_data = elemEfcTranslationData;
            break;
        case "Area":
            translation_data = areaTranslationData;
            break;
        default:
            return "";
    }

    if (!translation_data[key]) {
        if (default_value) return default_value;
        else return key;
    }
    return translation_data[key];
}

